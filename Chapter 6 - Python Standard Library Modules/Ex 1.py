import math

a = 2.3
ceil_value = math.ceil(a)
print(f"Ceil of {a} is: {ceil_value}")

#for a = 2.3, find the floor of a
floor_value = math.floor(a)
print(f"Floor of {a} is: {floor_value}")

#for a = 5, find the factorial of a
factorial_value = math.factorial(5)
print(f"Factorial of 5 is: {factorial_value}")

#find the value of 2^3
power_value = math.pow(2, 3)
print(f"2^3 is: {power_value}")

#for a = 16, find the square root of a
square_root_value = math.sqrt(16)
print(f"Square root of {a} is: {square_root_value}")